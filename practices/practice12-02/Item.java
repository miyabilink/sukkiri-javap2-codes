public class Item {
    private String name; 
    private int price;
    private int weight;
    public String getName() {
    	return this.name;
    }
    public int getPrice() {
    	return this.price;
    }
    public int getWeight() {
    	return this.weight;
    }
    public void setName(String name) {
    	this.name = name;
    }
    public void setPrice(int price) {
    	this.price = price;
    }
    public void setWeight(int weight) {
    	this.weight= weight;
    }
}